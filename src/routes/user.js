const express = require('express');
const router = express.Router();
const mongoose = require('mongoose');
//Import user schema
const user = require('../models/user.js');
const settings = require("../models/settings.js");

//user login api
router.post('/login', (req, res) => {
    //find user
    if (user === null) {
        return res.status(400).send({
            message : 'User Not Found.'
        });
    } //End if

    else {
        if (user.validPass(req.body.password)) {
            return res.status(201).send({
                message : 'User Logged In.'
            });
        } //end inner if

        else {
            return res.status(400).send({
                message : 'Wrong Password.'
            });
        } //End inner else
    } //End else
});

//user signup api
router.post('/register', (req, res, next) => {

    const newUser = new user({
        //initialize newUser ID with autogenerated ID
        _id: new mongoose.Types.ObjectId(),
        
        //initialize newUser with request data
        firstName: req.body.user.firstName,

        lastName: req.body.user.lastName,

        userName: req.body.user.userName,

        email: req.body.user.email,

        password: req.body.user.password
    });

    //call setPass function to hash password
    newUser.setPass(req.body.user.password);

    //save the new user to the DB
    newUser.save()
        .then(settings => {
            res.status(201).send({
                message : "New user added successfully"
            });
        })
        .catch(err => {
            console.error(err);
            res.status(400).send({
                message : "Failed to add new user"
            });
    });

    // Create settings document for the user
    const newSettings = new settings({
        // Link the settings to the user
        userID: newUser._id 
    });

    newSettings.save();

});

module.exports = router;